package week10.sunday;

public class Calculator {

    public int sumUpTwoNumbers(int num1, int num2) {
        return num1 + num2;
    }

    public int subtractTwoNumbers(int num1, int num2) {
        return num1 - num2;
    }

    public int multiplyTwoNumbers(int num1, int num2) {
        return num1 * num2;
    }

    public int divideTwoNumbers(int num1, int num2) {
        return num1 / num2;
    }

    public int modTwoNumbers(int num1, int num2) {
        return num1 % num2;
    }
}
